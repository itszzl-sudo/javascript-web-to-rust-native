#!/usr/bin/env node

const fs = require('fs');
const path = require('path');
const { Director } = require('../src/director');

const VERSION = '0.1.0';

async function main() {
  const args = process.argv.slice(2);
  
  if (args.length === 0 || args[0] === '--help' || args[0] === '-h') {
    printHelp();
    return;
  }
  
  if (args[0] === '--version' || args[0] === '-v') {
    console.log(`@irisverse/jade v${VERSION}`);
    return;
  }
  
  const command = args[0];
  
  switch (command) {
    case 'init':
      await handleInit(args.slice(1));
      break;
    case 'build':
      await handleBuild(args.slice(1));
      break;
    case 'dev':
      await handleDev(args.slice(1));
      break;
    case 'link':
      await handleLink(args.slice(1));
      break;
    case 'pack':
      await handlePack(args.slice(1));
      break;
    case 'mobile':
      await handleMobile(args.slice(1));
      break;
    case 'serve':
      await handleServe(args.slice(1));
      break;
    default:
      // 编译模式（向后兼容）
      if (command.endsWith('.js') || command.endsWith('.ts')) {
        await handleCompile(args);
      } else {
        console.error(`❌ Unknown command: ${command}`);
        console.error('Run `jade --help` for usage.');
        process.exit(1);
      }
  }
}

// jade init <project-name>
async function handleInit(args) {
  let projectName = 'my-app';
  let template = 'vue';
  
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '-t' || arg === '--template') {
      template = args[++i];
    } else if (!arg.startsWith('-')) {
      projectName = arg;
    }
  }
  
  console.log(`\n🚀 Creating ${projectName}...\n`);
  
  const projectDir = path.resolve(projectName);
  
  // 创建目录结构
  const dirs = [
    '',
    'src',
    'public',
    'dist'
  ];
  
  for (const dir of dirs) {
    const fullPath = path.join(projectDir, dir);
    if (!fs.existsSync(fullPath)) {
      fs.mkdirSync(fullPath, { recursive: true });
    }
  }
  
  // 生成 package.json
  const packageJson = {
    name: projectName,
    version: '0.1.0',
    scripts: {
      'dev': 'jade dev',
      'build': 'jade build',
      'mobile:ios': 'jade mobile ios',
      'mobile:android': 'jade mobile android'
    },
    jade: {
      input: 'src/index.js',
      output: 'dist',
      target: 'desktop'
    }
  };
  fs.writeFileSync(
    path.join(projectDir, 'package.json'),
    JSON.stringify(packageJson, null, 2)
  );
  
  // 生成 jade.config.json
  const jadeConfig = {
    input: 'src/index.js',
    output: 'dist',
    name: projectName,
    target: 'desktop',
    embed: false,
    minify: true,
    sourceMap: true,
    template: template
  };
  fs.writeFileSync(
    path.join(projectDir, 'jade.config.json'),
    JSON.stringify(jadeConfig, null, 2)
  );
  
  // 生成示例源码
  const indexJs = getTemplateCode(template);
  fs.writeFileSync(path.join(projectDir, 'src/index.js'), indexJs);
  
  // README
  const readme = `# ${projectName}

Generated by @irisverse/jade

## Usage

\`\`\`bash
npm run dev      # 开发模式
npm run build    # 构建
npm run mobile:ios     # 构建 iOS
npm run mobile:android # 构建 Android
\`\`\`
`;
  fs.writeFileSync(path.join(projectDir, 'README.md'), readme);
  
  console.log(`✅ Created ${projectName}/`);
  console.log(`
Next steps:
  cd ${projectName}
  npm install
  npm run build
`);
}

// jade build [options]
async function handleBuild(args) {
  const config = loadConfig();
  
  let inputFile = config.input || 'src/index.js';
  let outputName = config.name || 'app';
  let target = config.target || 'desktop';
  let embed = config.embed || false;
  let outputDir = config.output || 'dist';
  
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '-i' || arg === '--input') {
      inputFile = args[++i];
    } else if (arg === '-n' || arg === '--name') {
      outputName = args[++i];
    } else if (arg === '-t' || arg === '--target') {
      target = args[++i];
    } else if (arg === '-o' || arg === '--output') {
      outputDir = args[++i];
    } else if (arg === '--embed' || arg === '-e') {
      embed = true;
    }
  }
  
  console.log(`\n📦 Building...\n`);
  console.log(`Input:  ${inputFile}`);
  console.log(`Output: ${outputDir}/${outputName}`);
  console.log(`Target: ${target}`);
  
  if (!fs.existsSync(inputFile)) {
    console.error(`❌ Input file not found: ${inputFile}`);
    process.exit(1);
  }
  
  const jsCode = fs.readFileSync(inputFile, 'utf-8');
  const director = new Director();
  
  try {
    const outputPath = await director.compile(jsCode, {
      outputName,
      embed,
      target,
      outputDir
    });
    
    console.log(`\n✅ Build complete!`);
    console.log(`Output: ${outputPath}`);
    
  } catch (error) {
    console.error(`\n❌ Build failed:`);
    console.error(error.message);
    process.exit(1);
  }
}

// jade dev [options]
async function handleDev(args) {
  const config = loadConfig();
  let inputFile = config.input || 'src/index.js';
  
  console.log(`\n🔥 Development mode\n`);
  console.log(`Watching: ${inputFile}`);
  console.log('Press Ctrl+C to stop\n');
  
  // 初始构建
  await handleBuild(['--embed']);
  
  // 监听文件变化
  let lastMtime = fs.statSync(inputFile).mtimeMs;
  
  setInterval(async () => {
    try {
      const mtime = fs.statSync(inputFile).mtimeMs;
      if (mtime > lastMtime) {
        lastMtime = mtime;
        console.log('\n📝 File changed, rebuilding...\n');
        await handleBuild(['--embed']);
      }
    } catch (e) {}
  }, 500);
}

// jade mobile <ios|android>
async function handleMobile(args) {
  const platform = args[0];
  
  if (!platform || !['ios', 'android', 'all'].includes(platform)) {
    console.error('❌ Specify platform: ios, android, or all');
    console.error('Usage: jade mobile <ios|android|all>');
    process.exit(1);
  }
  
  console.log(`\n📱 Building for ${platform}...\n`);
  
  const config = loadConfig();
  
  // 设置移动端目标
  const target = platform === 'ios' ? 'aarch64-apple-ios' :
                 platform === 'android' ? 'aarch64-linux-android' :
                 'mobile';
  
  await handleBuild(['--target', target]);
  
  console.log(`\n✅ ${platform} build complete!`);
  console.log(`Output: dist/${platform}/`);
}

// jade serve [port]
async function handleServe(args) {
  const port = parseInt(args[0]) || 3000;
  const http = require('http');
  
  console.log(`\n🌐 Server running at http://localhost:${port}\n`);
  
  http.createServer((req, res) => {
    const filePath = path.join('dist', req.url === '/' ? 'index.html' : req.url);
    if (fs.existsSync(filePath)) {
      res.end(fs.readFileSync(filePath));
    } else {
      res.statusCode = 404;
      res.end('Not found');
    }
  }).listen(port);
}

async function handleLink(args) {
  let objPath = null;
  let libPath = null;
  let outputPath = 'app.exe';
  
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '-o' || arg === '--output') {
      outputPath = args[++i];
    } else if (arg === '-l' || arg === '--lib') {
      libPath = args[++i];
    } else if (!arg.startsWith('-')) {
      if (!objPath) objPath = arg;
      else if (!libPath) libPath = arg;
    }
  }
  
  if (!objPath) {
    console.error('❌ No object file specified');
    process.exit(1);
  }
  
  const director = new Director();
  const result = await director.link(objPath, libPath || '', outputPath);
  console.log(`\n✅ Linked: ${result}`);
}

async function handlePack(args) {
  let objPath = null;
  let libName = 'mylib';
  
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '-n' || arg === '--name') {
      libName = args[++i];
    } else if (!arg.startsWith('-')) {
      objPath = arg;
    }
  }
  
  if (!objPath) {
    console.error('❌ No object file specified');
    process.exit(1);
  }
  
  const director = new Director();
  const result = await director.packLib(objPath, libName);
  console.log(`\n✅ Packed: ${result}`);
}

async function handleCompile(args) {
  let inputFile = null;
  let outputName = 'app';
  let embed = false;
  
  for (let i = 0; i < args.length; i++) {
    const arg = args[i];
    if (arg === '--input' || arg === '-i') {
      inputFile = args[++i];
    } else if (arg === '--name' || arg === '-n') {
      outputName = args[++i];
    } else if (arg === '--embed' || arg === '-e') {
      embed = true;
    } else if (!arg.startsWith('-')) {
      inputFile = arg;
    }
  }
  
  if (!inputFile) {
    console.error('❌ No input file specified');
    process.exit(1);
  }
  
  const jsCode = fs.readFileSync(inputFile, 'utf-8');
  console.log(`Input: ${inputFile} (${jsCode.length} bytes)\n`);
  
  const director = new Director();
  const outputPath = await director.compile(jsCode, { outputName, embed });
  
  console.log(`\n✅ Success!`);
  console.log(`Output: ${outputPath}`);
}

function loadConfig() {
  const configPath = path.resolve('jade.config.json');
  if (fs.existsSync(configPath)) {
    return JSON.parse(fs.readFileSync(configPath, 'utf-8'));
  }
  return {};
}

function getTemplateCode(template) {
  const templates = {
    vue: `// Vue 3 App
import { createApp, ref } from 'vue';

createApp({
  setup() {
    const count = ref(0);
    return { count };
  },
  template: \`
    <div>
      <h1>Hello Jade!</h1>
      <p>Count: {{ count }}</p>
      <button @click="count++">Increment</button>
    </div>
  \`
}).mount('#app');
`,
    react: `// React App
import React, { useState } from 'react';

function App() {
  const [count, setCount] = useState(0);
  return (
    <div>
      <h1>Hello Jade!</h1>
      <p>Count: {count}</p>
      <button onClick={() => setCount(count + 1)}>Increment</button>
    </div>
  );
}

export default App;
`,
    vanilla: `// Vanilla JS
document.body.innerHTML = \`
  <div>
    <h1>Hello Jade!</h1>
    <p>Count: <span id="count">0</span></p>
    <button id="btn">Increment</button>
  </div>
\`;

let count = 0;
document.getElementById('btn').onclick = () => {
  count++;
  document.getElementById('count').textContent = count;
};
`
  };
  
  return templates[template] || templates.vanilla;
}

